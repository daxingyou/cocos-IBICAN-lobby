const { ccclass, property } = cc._decorator;

@ccclass
export default class BaseTipView extends JPView {
    @property(cc.Label)
    public tipContent:cc.Label = null

    public data:any;

    public show(tip: TipsInfo): void {
        if(!tip) tip = '';
        this.tipContent.string = tip;
    }
}
